IOWA.CountdownTimer.Core.prototype.getLayout = function() {
    var t = this.containerDomElement.offsetWidth
      , e = "horizontal" === this.format ? this.containerDomElement.offsetWidth / 2 : this.containerDomElement.offsetWidth;
    t < IOWA.CountdownTimer.MOBILE_BREAKPOINT ? (this.countdownMargin = 14,
    this.bandGutter = 16,
    this.bandPadding = 4) : t < IOWA.CountdownTimer.MOBILE_MAX_BREAKPOINT ? (this.countdownMargin = 14,
    this.bandGutter = 16,
    this.bandPadding = 4) : t < IOWA.CountdownTimer.TABLET_BREAKPOINT ? (this.countdownMargin = 40,
    this.bandGutter = 16,
    this.bandPadding = 4) : t < this.maxWidth ? (this.countdownMargin = 4,
    this.bandGutter = 16,
    this.bandPadding = 4) : t > this.maxWidth && (this.countdownMargin = Math.round((t - this.maxWidth) / 2),
    this.bandGutter = 32,
    this.bandPadding = 8),
    t < IOWA.CountdownTimer.MOBILE_BREAKPOINT ? this.strokeWeight = 2.5 : t < IOWA.CountdownTimer.MOBILE_MAX_BREAKPOINT ? this.strokeWeight = 3 : t < IOWA.CountdownTimer.TABLET_BREAKPOINT ? this.strokeWeight = 2.5 : t < IOWA.CountdownTimer.DESKTOP_BREAKPOINT ? this.strokeWeight = 3 : t < IOWA.CountdownTimer.XLARGE_BREAKPOINT && (this.strokeWeight = 4);
    var i = t - 2 * this.countdownMargin
      , n = e
      , r = (i - 3 * this.bandGutter - 4 * this.bandPadding) / 8 / 2
      , s = this.countdownMargin
      , o = n / 2 - r;
    "horizontal" === this.format && (o -= IOWA.CountdownTimer.CENTER_OFFSET),
    "stacked" === this.format && (r = (i - this.bandGutter - 2 * this.bandPadding) / 4 / 2),
    this.layout = {
        x: s,
        y: o,
        radius: r
    }
}
