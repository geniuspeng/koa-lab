IOWA.CountdownTimer.Core.prototype.resetCanvas = function() {
    if (!this.drawAll) {
        var t = this.canvasElement.getContext("2d");
        t.clearRect(0, 0, this.canvasElement.width, this.canvasElement.height)
    }
    this.getFormat(),
    this.setCanvasSize(),
    this.getLayout(),
    this.unitsAdded = !1;
    for (var e = 0; e < this.bands.length; e++)
        this.bands[e].radius = this.layout.radius,
        this.bands[e].center = this.getBandCenter(e),
        this.bands[e].redraw();
    this.intro && (this.intro.radius = this.layout.radius,
    this.intro.center = "horizontal" === this.format ? this.getBandCenter(1) : this.getBandCenter(5)),
    this.getSeparators(),
    this.needsCanvasReset = !1
}
