IOWA.CountdownTimer.Core.prototype.onFrame = function() {
    if (this.isPlaying) {
        if (this.needsCanvasReset && this.resetCanvas(),
        this.intro)
            return this._onIntroFrame(),
            void requestAnimationFrame(this.onFrame);
        this.mouseCoords && this.handleMouseShudder(),
        this.checkTime();
        var t, e = this.canvasElement.getContext("2d");
        for (e.save(),
        e.scale(this.pixelRatio, this.pixelRatio),
        this.drawAll && e.clearRect(0, 0, this.canvasElement.width, this.canvasElement.height),
        e.restore(),
        this.currentlyRandomizing || this.unitsAdded && !this.drawAll || (this.addUnits(),
        this.unitsAdded = !0),
        t = 0; t < this.bands.length; t++)
            this.bands[t].update();
        this.currentlyRandomizing || "horizontal" !== this.format || this.addSeparators(),
        requestAnimationFrame(this.onFrame)
    }
}
