IOWA.CountdownTimer.Core.prototype.addSeparators = function() {
    var t = this.canvasElement.getContext("2d");
    if (t.save(),
    t.scale(this.pixelRatio, this.pixelRatio),
    this.showCurrentTime) {
        t.fillStyle = "#78909C";
        for (var e = 0; e < this.colonSeparators.length; e++)
            t.clearRect(this.colonSeparators[e].x - this.colonSeparators[e].radius - 2, this.colonSeparators[e].y - this.colonSeparators[e].radius - 2, 2 * this.colonSeparators[e].radius + 4, 2 * this.colonSeparators[e].radius + 4),
            t.beginPath(),
            t.arc(this.colonSeparators[e].x, this.colonSeparators[e].y, this.colonSeparators[e].radius, 0, 2 * Math.PI),
            t.fill()
    } else {
        t.strokeStyle = "#78909C";
        for (var i = 0; i < this.commaSeparators.length; i++)
            t.clearRect(this.commaSeparators[i].x - 2, this.commaSeparators[i].y - 2, this.commaSeparators[i].w + 4, this.commaSeparators[i].h + 4),
            t.beginPath(),
            t.moveTo(this.commaSeparators[i].x, this.commaSeparators[i].y),
            t.lineTo(this.commaSeparators[i].x + this.commaSeparators[i].w, this.commaSeparators[i].y + this.commaSeparators[i].h),
            t.lineWidth = this.strokeWeight,
            t.stroke()
    }
    t.restore()
}
