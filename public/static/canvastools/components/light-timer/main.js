IOWA.CountdownTimer.Core.prototype.nextColumnValues = function(t, e) {
    if (this.showCurrentTime) {
        var i = new Date;
        return {
            column1: i.getHours(),
            column2: i.getMinutes(),
            column3: i.getSeconds()
        }
    }
    var n = new Date(t - e).getTime() / 1e3
      , r = 60
      , s = 60 * r
      , o = 24 * s
      , a = Math.floor(n / o);
    n %= o;
    var l = Math.floor(n / s);
    n %= s;
    var A = Math.floor(n / r);
    n %= r;
    var h = Math.floor(n);
    return {
        column1: a,
        column2: l,
        column3: A,
        column4: h
    }
}
