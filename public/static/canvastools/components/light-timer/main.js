IOWA.CountdownTimer.Core.prototype.getSeparators = function() {
    this.commaSeparators = [],
    this.colonSeparators = [];
    for (var t = 1; t <= 2; t++) {
        var e = this.bands[2 * t].center.x - this.layout.radius - (this.bandPadding + this.bandGutter) / 2
          , i = this.bands[2 * t].center.y + this.layout.radius - this.bandGutter / 1.6;
        this.commaSeparators.push({
            x: e,
            y: i,
            w: this.bandGutter / 2,
            h: this.bandGutter / 1.8
        })
    }
    for (var n = 1; n <= 2; n++) {
        var r = Math.round(this.bands[2 * n].center.x - this.layout.radius - this.bandGutter / 2)
          , s = Math.round(this.bands[2 * n].center.y - .5 * this.layout.radius)
          , o = Math.round(this.bands[2 * n].center.y + .5 * this.layout.radius)
          , a = Math.round(this.bandGutter / 4);
        this.colonSeparators.push({
            x: r,
            y: s,
            radius: a
        }),
        this.colonSeparators.push({
            x: r,
            y: o,
            radius: a
        })
    }
}
